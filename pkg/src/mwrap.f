      SUBROUTINE MWRAP(N,WT,NMATCH)
      
      INTEGER BASIS(5001),MEM(5001),KA(5001)
      INTEGER KB(5001),ZFW,NMATCH(5001),CC(26000000)
      INTEGER SM(5001),TMA(5001),TMB(5001),INDEX(5001)
      INTEGER NBL(26000000), WT(26000000)
      INTEGER M1,NN,X,Y,N,M,I
      DOUBLE PRECISION  Y1(5001),Y2(5001),DMINUS(5001),DPLUS(5001)
  
      M=N*(N-1)/2
      M1=2*M
      NN=0
      DO 25 X=1,N
      DO 35 Y=1,N
      IF(Y.Eq. X) go to 35
      NN=NN+1
      NBL(NN)=Y
      CC(NN)=WT((X-1)*N+Y)
 35   CONTINUE
      INDEX(X)=(X-1)*(N-1)+1
 25   CONTINUE
      INDEX(N+1)=N*(N-1)+1      

      EPS=10.**(-38)
      SUP=40000000
      CALL SAP(N,M,CC,NBL,INDEX,ZFW,
     *NMATCH,BASIS,MEM,KA,KB,SM,
     *TMA,TMB,Y1,Y2,DPLUS,DMINUS,
     *SUP,EPS)


      END SUBROUTINE
